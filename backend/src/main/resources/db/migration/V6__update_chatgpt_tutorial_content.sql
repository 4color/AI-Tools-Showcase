## 注意事项

### 1. 信息准确性

ChatGPT可能生成不准确或过时的信息，特别是：
- 实时数据（如当前股价、天气）
- 专业知识（如医疗、法律建议）
- 最新事件

**建议**：重要信息务必验证来源。

### 2. 隐私保护

- 不要输入个人敏感信息
- 不要分享密码、API密钥等
- 注意企业数据安全政策

### 3. 版权问题

- ChatGPT生成的内容可能不完全原创
- 商业使用需注意版权问题
- 学术使用需标注AI辅助

### 4. 使用限制

免费版可能有的限制：
- 响应速度较慢
- 高峰期可能无法访问
- 功能相对有限

